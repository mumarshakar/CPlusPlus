#include<iostream>
using namespace std;

int main()
{
	double gallons, miles, mpg;

	//No Of Gallons
	
	gallons = 12;
	
	// No of miles travelled
	miles = 350;
	
	// No of miles per gallon (MPG)
	
	mpg = miles / gallons;
	
	// Print Results 
	
	cout << "No Of Miles Per Gallon: " << mpg << endl;
	
	return 0;
}
