#include<iostream>
using namespace std;

int main(){
	
	double speed ;
	double time;
	double distance;
	
	// Store Speed 20 in Speed Variable 
	speed = 20;
	// Store time 10 in Speed Variable 
	time = 10;
	
	// Calculate Distance By Using Formula 
	
	distance = speed * time;
	
	// Printing the resultant Distance 
	
	cout << "Total Distance : \t" << distance << " m/s (Meters Per Seconds)" << endl ;
	
	return 0;
		

}
