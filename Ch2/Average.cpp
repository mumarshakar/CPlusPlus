#include<iostream>

using namespace std;

int main()
{
	double n1, n2, n3, n4, n5, sum, average;
	n1 = 28;
	n2 = 32;
	n3 = 37;
	n4 = 24;
	n5 = 33;
	
	// Calculation Of Sum
	sum = n1+n2+n3+n4+n5;
	
	//Calculation Of Avarage 
	average = sum/5;
	
	// Showing the Results
	cout << "Average:" << average << endl; 
}
